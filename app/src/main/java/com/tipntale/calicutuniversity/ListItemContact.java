package com.tipntale.calicutuniversity;

public class ListItemContact {

    private String name;
    private String number;

    public ListItemContact(String name, String number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }


}
